package by.alhr.hw2_Task3.bean;

public class MachOperations {
    public int a;
    public int b;
    public double summ(int a,int b) {

        return a + b;
    }
    public double difference (int a,int b) {

        return a - b;
    }
    public double multiplication (int a,int b) {

        return a * b;
    }
    public double division(int a,int b) {

        return (double) a / b;
    }
}
