package by.seven;

public class MathOperationsDemon {
    public static void main(String[] args) {
        int a = 36;
        double b = 9.0;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("b + b = " + (b + b));
        System.out.println("b * a = " + (b * a));
        System.out.println("a / b = " + (a / b));

    }
}
